function booleanOperators() {
  console.log(3 > 5);
  console.log(3 > 5);
  console.log(12 / 2 == 6);
  console.log(12 / 3 != 4);
  console.log(7 <= 3);
  console.log(4 == 2 * 2 && 2 == 3 / 2);
  console.log(false || true);
  console.log(true && true);

  // In JavaScript, there's the === operator for equivalence in value & type
  console.log(3 == '3');
  console.log(3 === '3');
  console.log(3 != '3');
  console.log(3 !== '3');
}

